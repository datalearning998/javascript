// Declare variables

let x = 10;  

let y = 5;   

// Addition
let sum = x + y;  // Adds x and y (10 + 5 = 15)
  console.log("Sum: " + sum);  

// Subtraction

let difference  = x -  y;  
console.log("Difference: " + difference);  //  Difference: 5

// Multiplication

let product = x * y;  
console.log("Product: " + product)  ;  

// Division
let quotient = x / y;  
console.log("Quotient: " + quotient);  


// Modulus Remainder of division
let remainder = x % y;  // Gives 0
console.log("Remainder: " + remainder);  


// Increment
x++;  // Increases the value of x by 1 
console.log("Incremented x: " + x); 

// Decrement
y--;  // Decreases the value of y by 1 
console.log("Decremented y: " + y);  

// Using constants
const z = 10;  
// z cannot be changed later in the code itss a constant



let total = x + y + z;  

console.log("Total: " + total);  